x = input()
x = int(x)

# Recap Conditional Expressions




# Construct 0 : if then

if x == 2:
    print("x is 2")

# Construct 1 : If then else

if x == 2:
    print("x is 2")
else:
    print("x is not true")

# Construct 2 : if then elif then elif then.... else

if x == 2:
    print("x is 2")
elif x == 3:
    print("x is 3")
elif x == 4:
    print("x is 4")
else:
    print("x is not true")

# Construct 3 : if then elif then elif then....

if x == 2:
    print("x is 2")
elif x == 3:
    print("x is 3")
elif x == 4:
    print("x is 4")
